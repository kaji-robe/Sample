
public class Forsample {

    public static void main(String[] args) {
        //iの値が0から21より小さい間、処理を繰り返す
        for (int i = 0; i < 21; i++) {
            //iの値が2で割り切れるとき
            if (i % 2 == 0) {
                System.out.println("iとは" + i);
            }

        }
        // iの値を表示する

    }

}
